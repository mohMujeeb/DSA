"""
Data Types in Python 
 - Integer (int store decimal numbers) -> int
 - Float   (float store decimal numbers in points) -> float
 - Boolean (bool store boolean values as true or false) -> bool
 - String  (string store sequence of characters) -> str
 
"""


integer_dt = 8
float_dt = 16.2
boolean_dt = True
string_dt = "abc"

print("Integer: ", integer_dt)
print("Float: ", float_dt)
print("Boolean: ", boolean_dt)
print("String: ", string_dt)

# Another way to check Data Types in Python is using type function 

print("Type of integer_dt: ", type(integer_dt))
print("Type of float_dt: ", type(float_dt))
print("Type of boolean_dt: ", type(boolean_dt))
print("Type of string_dt: ", type(string_dt))